//both type & value assigned 
var userName:string = 'Bilal'
console.log(userName)

//only value assigned
var test1 = 'Ahmed'
console.log(test1)

//only type assigned
var test2:number; 
test2 = 10;
console.log('1st value = ',test2)

// var value can be changed later
var test2 = 20
console.log('updated value = ',test2)

// 'let' or 'const' value can't be updated later
let test3 = 5;
let test3 = 6;

console.log(test3)